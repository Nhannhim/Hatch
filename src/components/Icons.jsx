export function Icon({ name, size = 14, color, glass = false, style = {} }) {
  const paths = P[name];
  if (!paths) return <span style={{ fontSize: size, ...style }}>{name}</span>;

  const c = color || "currentColor";
  const hasFill = FILLS[name];
  const gradId = glass ? `lg_${name}_${++gradientIdCounter}` : null;
  const strokeColor = glass ? `url(#${gradId})` : c;

  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={hasFill ? c : "none"}
      stroke={hasFill ? "none" : strokeColor}
      strokeWidth="1.5"
      strokeLinecap="round"
      strokeLinejoin="round"
      style={{ display: "inline-block", verticalAlign: "middle", flexShrink: 0, ...style }}
    >
      {glass && <GlassGradient id={gradId} color={c} />}
      {paths.map((d, i) => (
        <path key={i} d={d} />
      ))}
    </svg>
  );
}

